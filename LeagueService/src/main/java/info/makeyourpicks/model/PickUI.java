package info.makeyourpicks.model;

public class PickUI {

	private long team;
	private long league;
	private long week;
	private long game;
	
	public long getTeam() {
		return team;
	}
	public void setTeam(long team) {
		this.team = team;
	}
	public long getLeague() {
		return league;
	}
	public void setLeague(long league) {
		this.league = league;
	}
	public long getWeek() {
		return week;
	}
	public void setWeek(long week) {
		this.week = week;
	}
	public long getGame() {
		return game;
	}
	public void setGame(long game) {
		this.game = game;
	}
	
	
}
